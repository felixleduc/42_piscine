int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	else if (nb == 2)
		return (1);
	else if (nb == 4)
		return (5);
	while (i < (nb / 2))
	{
		if (nb % i == 0)
			return (0);
		++i;
	}
	return (1);
}
