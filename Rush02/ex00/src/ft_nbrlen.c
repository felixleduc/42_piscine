#include"fct.h"

int	ft_nbrlen(char *str)
{
	int	len;
	int	i;

	len = 0;
	i = 0;
	while (str[i] && !(str[i] >= '0' && str[i] <= '9'))
		++i;
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		++len;
		++i;
	}
	return (len);
}
