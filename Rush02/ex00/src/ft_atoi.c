#include"fct.h"

int	ft_atoi(char *str)
{
	int	i;
	int	digit;

	i = 0;
	digit = 0;
	while (str[i] && !(str[i] >= '0' && str[i] <= '9'))
		++i;
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		digit = digit * 10 + (str[i] - '0');
		++i;
	}
	return (digit);
}
